public class int_aritmetica {
	public static void main(String args[]) {
		int a = 100;
		int b = 30;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(-a);
		System.out.println(a%b);
		System.out.println(a&b);
		System.out.println(a<<2);
		System.out.println(a>>2);
		a = -100;
		System.out.println(a>>2);
		a = -1>>>2;
		System.out.println(a);
	}
}
